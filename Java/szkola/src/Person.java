public class Person {
    private String mName;
    private int  mAge;
    public  Person(String name, int age){
        mName = name;
        mAge = age;

    }

    public String getmName() {
        return mName;
    }

    public void setmName(String mName) {
        this.mName = mName;
    }

    public int getmAge() {
        return mAge;
    }

    public void setmAge(int mAge) {
        this.mAge = mAge;
    }
}
